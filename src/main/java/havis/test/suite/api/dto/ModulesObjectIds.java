package havis.test.suite.api.dto;

import java.util.List;

public class ModulesObjectIds {
	private List<String> apps;
	private List<String> reporters;
	private List<String> statisticCreators;
	
	public List<String> getApps() {
		return apps;
	}
	public void setApps(List<String> apps) {
		this.apps = apps;
	}
	public List<String> getReporters() {
		return reporters;
	}
	public void setReporters(List<String> reporters) {
		this.reporters = reporters;
	}
	public List<String> getStatisticCreators() {
		return statisticCreators;
	}
	public void setStatisticCreators(List<String> statisticCreators) {
		this.statisticCreators = statisticCreators;
	}
	
	
	
	
}
